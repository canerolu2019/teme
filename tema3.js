// Function that takes 2 paramiters (values)
function maxOfTwo(first, second) {
  // If block compares if the first value is smaller the second
  if (first < second) {
    // If the condition is true, then returns second as greater then first
    return second;
  } else {
    // If the condition is false, then returns first as greater then second
    return first;
  }
}